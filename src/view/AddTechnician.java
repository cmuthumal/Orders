    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        name = nameTextField.getText();
        boolean added = false;

        if (!name.equals("") && (new Extra().validateText(name))) {
            try {
                id = new TechnicianController().getNextID();
                Technician tech = new Technician(id, name);
                added = new TechnicianController().addTechnician(tech);
            } catch (SQLException ex) {
                Logger.getLogger(AddTechnician.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddTechnician.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (added) {
                JOptionPane.showMessageDialog(rootPane, "Technician added.", "Done", 1);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Enter Technician Name.", "Error", 1);
        }
    }//GEN-LAST:event_addButtonActionPerformed

