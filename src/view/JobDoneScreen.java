    private void setTable() {
        jobTableModel.setRowCount(0);
        if (!jobsList.isEmpty()) {
            for (int i = 0; i < jobsList.size(); i++) {
                Job job = jobsList.get(i);

                Object[] row = {job.getNoteNo(), job.getDate(), getBrandByID(job.getBrandID()), job.getModel(), job.getErrorDesc(), getCustomerByID(job.getCustomerID())};
                jobTableModel.addRow(row);
            }
        }
    }

