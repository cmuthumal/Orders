    private void setAllTechniciansToCombo() {
        try {
            techniciansList = technicianController.getAllTechnicians();
        } catch (SQLException ex) {
            Logger.getLogger(AddItem.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddItem.class.getName()).log(Level.SEVERE, null, ex);
        }
        setTechniciansCombo();
    }

    private void setTechniciansCombo() {
        if (!techniciansList.isEmpty()) {
            techniciansArray = new String[techniciansList.size()];
            for (int i = 0; i < techniciansList.size(); i++) {
                Technician tech = techniciansList.get(i);
                techniciansArray[i] = tech.getName();
            }
        } else {
            techniciansArray = new String[1];
            techniciansArray[0] = "-No matching results-";
        }
        techniciansCombo.setModel(new DefaultComboBoxModel(techniciansArray));
    }
