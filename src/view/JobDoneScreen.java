    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        int warrenty = 0, cost = 0, sellingPrice = 0, technicianID;
        String date = "", remarks = "";
        boolean dateSet = false, warrentySet = false, costSet = false, sellingPriceSet = false;

        Date d = issuedDateChooser.getDate();
        try {
            date = dateFormat.format(d);
            dateSet = new Extra().validateDate(date);
        } catch (NullPointerException e) {
            dateSet = false;
        }
        if (!warrantyTextField.getText().equals("")) {
            warrenty = Integer.parseInt(warrantyTextField.getText());
            warrentySet = true;
        }
        if (!costTextField.getText().equals("")) {
            cost = Integer.parseInt(costTextField.getText());
            costSet = true;
        }
        if (!sellingPriceTextField.getText().equals("")) {
            sellingPrice = Integer.parseInt(sellingPriceTextField.getText());
            sellingPriceSet = true;
        }
        technicianID = techniciansCombo.getSelectedIndex() + 1;

        if (dateSet && noteNoSet && warrentySet && costSet && sellingPriceSet) {
            int id = 0;
            try {
                id = new JobController().getNextID();
            } catch (SQLException ex) {
                Logger.getLogger(JobDone.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JobDone.class.getName()).log(Level.SEVERE, null, ex);
            }

            remarks = remarksTextField.getText();

            JobDone jobDone = new JobDone(id, selectedNoteNo, date, warrenty, remarks, cost, sellingPrice, technicianID);
            boolean added = false;

            try {
                added = new JobDoneController().addJobDone(jobDone);

                if (added) {
                    JOptionPane.showMessageDialog(rootPane, "Job added.", "Done", 1);
                    clearFields();
                    searchButtonActionPerformed(evt);
                }
            } catch (SQLException ex) {
                Logger.getLogger(JobDoneScreen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JobDoneScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (!noteNoSet) {
            JOptionPane.showMessageDialog(rootPane, "Select an item first.", "Error", 1);
        } else if (!dateSet) {
            JOptionPane.showMessageDialog(rootPane, "Select a date.", "Error", 1);
        } else if (!warrentySet) {
            JOptionPane.showMessageDialog(rootPane, "Enter warrenty period.", "Error", 1);
        } else if (!costSet) {
            JOptionPane.showMessageDialog(rootPane, "Enter cost.", "Error", 1);
        } else if (!sellingPriceSet) {
            JOptionPane.showMessageDialog(rootPane, "Enter selling price.", "Error", 1);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

